from datetime import datetime
from flask_sqlalchemy import SQLAlchemy

# This will be initialized from app.py
db = SQLAlchemy()

class User(db.Model):
    __tablename__ = 'users'
    
    id = db.Column(db.Integer, primary_key=True)
    email = db.Column(db.String(255), unique=True, nullable=False)
    name = db.Column(db.String(255), nullable=False)
    microsoft_user_id = db.Column(db.String(255), unique=True, nullable=False)
    refresh_token = db.Column(db.Text, nullable=True)  # Encrypted
    access_token = db.Column(db.Text, nullable=True)   # Encrypted
    token_expires_at = db.Column(db.DateTime, nullable=True)
    
    # User preferences
    tone_profile = db.Column(db.String(50), default='professional')  # professional, friendly, formal
    reply_length = db.Column(db.String(20), default='medium')       # short, medium, long
    auto_reply_enabled = db.Column(db.Boolean, default=False)
    
    # Course/context information
    course_policies = db.Column(db.Text, nullable=True)
    signature = db.Column(db.Text, nullable=True)
    
    created_at = db.Column(db.DateTime, default=datetime.utcnow)
    updated_at = db.Column(db.DateTime, default=datetime.utcnow, onupdate=datetime.utcnow)
    
    # Relationships
    emails = db.relationship("Email", back_populates="user", lazy=True)
    
    def to_dict(self):
        return {
            'id': self.id,
            'email': self.email,
            'name': self.name,
            'tone_profile': self.tone_profile,
            'reply_length': self.reply_length,
            'auto_reply_enabled': self.auto_reply_enabled,
            'created_at': self.created_at.isoformat() if self.created_at else None,
            'updated_at': self.updated_at.isoformat() if self.updated_at else None
        }

class Email(db.Model):
    __tablename__ = 'emails'
    
    id = db.Column(db.Integer, primary_key=True)
    user_id = db.Column(db.Integer, db.ForeignKey('users.id'), nullable=False)
    microsoft_message_id = db.Column(db.String(255), unique=True, nullable=False)
    
    # Email content
    subject = db.Column(db.String(500), nullable=False)
    sender_email = db.Column(db.String(255), nullable=False)
    sender_name = db.Column(db.String(255), nullable=True)
    body = db.Column(db.Text, nullable=False)
    body_preview = db.Column(db.String(500), nullable=True)
    
    # Email metadata
    received_at = db.Column(db.DateTime, nullable=False)
    importance = db.Column(db.String(20), default='normal')  # low, normal, high
    is_read = db.Column(db.Boolean, default=False)
    has_attachments = db.Column(db.Boolean, default=False)
    
    # Processing status
    processing_status = db.Column(db.String(50), default='pending')  # pending, processing, processed, failed
    ai_category = db.Column(db.String(100), nullable=True)  # Generated by AI
    priority_level = db.Column(db.Integer, default=3)  # 1-5 scale
    
    # Reply tracking
    requires_reply = db.Column(db.Boolean, default=True)
    reply_generated = db.Column(db.Boolean, default=False)
    reply_sent = db.Column(db.Boolean, default=False)
    reply_sent_at = db.Column(db.DateTime, nullable=True)
    
    # Timestamps
    created_at = db.Column(db.DateTime, default=datetime.utcnow)
    updated_at = db.Column(db.DateTime, default=datetime.utcnow, onupdate=datetime.utcnow)
    processed_at = db.Column(db.DateTime, nullable=True)
    
    # Relationships
    user = db.relationship("User", back_populates="emails")
    replies = db.relationship("EmailReply", back_populates="email", lazy=True)
    
    def to_dict(self):
        return {
            'id': self.id,
            'subject': self.subject,
            'sender_email': self.sender_email,
            'sender_name': self.sender_name,
            'body_preview': self.body_preview,
            'received_at': self.received_at.isoformat() if self.received_at else None,
            'importance': self.importance,
            'is_read': self.is_read,
            'has_attachments': self.has_attachments,
            'processing_status': self.processing_status,
            'ai_category': self.ai_category,
            'priority_level': self.priority_level,
            'requires_reply': self.requires_reply,
            'reply_generated': self.reply_generated,
            'reply_sent': self.reply_sent,
            'reply_sent_at': self.reply_sent_at.isoformat() if self.reply_sent_at else None
        }

